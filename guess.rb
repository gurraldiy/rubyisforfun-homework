number = rand(1..1_000_000)
puts "I picked the number from 1 to 1_000_000, guess it!"

loop do
  choice = gets.to_i

  if choice == number
    puts "Congratulations! You do it"
    exit
  end

  if choice > number
    puts "Need lesser number"
  else
    puts "Need greater number"
  end
end
