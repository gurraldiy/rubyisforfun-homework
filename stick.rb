#Print a spinning "cursor" in the console.



#===============================================================================================================
#                                               Solution
#===============================================================================================================

loop do
  print "/\r"
  sleep 0.01
  print "-\r"
  sleep 0.01
  print "\\\r"
  sleep 0.01
  print "|\r"
  sleep 0.01
end
